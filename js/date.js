function review(data, num) {
  // 找到要添加到的目标容器（示例中假设有 id 为 "targetContainer1" 的元素）
  var targetContainer = document.getElementById("targetContainer" + num);
  if (targetContainer) {
    var parentContainer = document.createElement("div");
    var items = [
      "2024-03-31",
      "2024-03-31",
      "2024-03-31",
      "2024-03-31",
      "2024-04-01",
      "2024-04-01",
      "2024-04-01",
      "2024-04-01",
      "2024-04-01",
    ];
    for (var i = 0; i < items.length; i++) {
      var item = items[i];
      var text = data[i];

      // 创建 <div class="row">
      var rowDiv = document.createElement("div");
      rowDiv.className = "row";

      // 创建 <span class="col"> 并添加 item 值
      var itemSpan = document.createElement("span");
      itemSpan.className = "col1";
      itemSpan.textContent = item;
      rowDiv.appendChild(itemSpan);

      // 创建 <span class="col"> 并添加固定文本
      var textSpan = document.createElement("span");
      textSpan.className = "col1";
      textSpan.textContent = text;
      rowDiv.appendChild(textSpan);

      // 添加点击事件监听器
      rowDiv.addEventListener("dblclick", function () {
        // 在新页面中打开链接
        window.open("https://s.weibo.com/top/summary", "_blank");
      });

      // 将重复的元素添加到父容器中
      parentContainer.appendChild(rowDiv);
    }

    // 清空目标容器的子元素
    targetContainer.innerHTML = "";

    // 将父容器添加到目标容器中
    targetContainer.appendChild(parentContainer);
  }
}

